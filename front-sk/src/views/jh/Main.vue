                        });
                })
                .catch(res => {
                    console.log('좋아요 게시글 조회 실패');
                });
        },
        getAlldata() {
            Axios.get(`${URI}/page/boardList`)
                .then(res => {
                    this.datas = res.data;
                })
                .catch(res => {
                    // console.log(res);
                });
        },
        addComment(info) {
            // console.log(this.comment);
            var commentObject = new Object();
            commentObject.boardid = info.boardid;
            commentObject.contents = this.comment;
            commentObject.uid = info.uid;
            if (this.comment == null) {
                alert('댓글을 입력해주세요');
            } else {
                Axios.post(`${URI}/page/comment`, commentObject)
                    .then(res => {
                        // console.log('댓글 달기 성공');
                        this.comment = '';
                    })
                    .catch(res => {
                        console.log('댓글 달기 실패');
                    });
                this.getAlldata();
            }
        },
        deleteComment(info) {
            if (confirm('댓글을 삭제하시겠습니까?')) {
                Axios.delete(`${URI}/page/comment`, {
                    data: info.commentid,
                })
                    .then(res => {
                        // console.log('댓글 삭제 성공');
                    })
                    .catch(res => {
                        console.log('댓글 삭제 실패');
                    });
                this.getAlldata();
            }
        },
        toggleLikeBtn(boardid) {
            Axios.post(`${URI}/page/favorite`, { jwt: jwt, boardid: boardid })
                .then(res => {
                    this.showAll();
                })
                .catch(res => {
                    console.log(res);
                });
        },
        toggleScrapBtn(boardid) {
            Axios.post(`${URI}/page/scrap`, { jwt: jwt, boardid: boardid })
                .then(res => {
                    this.showAll();
                })
                .catch(res => {
                    console.log(res);
                });
        },
    },
};
